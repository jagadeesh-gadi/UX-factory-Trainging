/* comparison Operators */

let x = 5;

// equal to ( == )
console.log("equal to of x is", x == 5);

// identical ( equal value and same type ) ( === )
console.log("identical value of x is", x === "5");

// not equal to ( != )
console.log("not equal to value of x is", x != 8);

// not identical ( !== )
console.log("not identical value of x is", x !== "5");

// greater than ( > )
console.log("greater than value of x is", x > 8);

// greater than equal to ( >= )
console.log("greater than equal to value of x is", x >= 9);

// less than ( < )
console.log("less than value of x is", x < 10);

// less than equal to ( <= )
console.log("less than equal to value of x is", x <= 2);

/* logical operators */

let a = 5;
let b = 10;
// Logical AND ( && )
console.log("Logical AND of a and b is", a < 10 && b > 8);

// Logical OR ( || )
console.log("Logical OR of a and b is", a < 10 || b > 20);

// Logical NOT ( ! )
console.log("Logical Not of and b is", !(a > b));

